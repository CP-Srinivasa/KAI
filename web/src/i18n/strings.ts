export const dicts: Record<Lang, Dict> = { de, en };

export function lookup(lang: Lang, key: string): string {
  const path = key.split(".");
  let cur: any = dicts[lang];
  for (const p of path) {
    if (cur == null) break;
    cur = cur[p];
  }
  if (typeof cur === "string") return cur;
  // fallback to en
  if (lang !== "en") {
    cur = dicts.en;
    for (const p of path) {
      if (cur == null) break;
      cur = cur[p];
    }
    if (typeof cur === "string") return cur;
  }
  return key;
}

export function interp(template: string, vars: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_m, k) => String(vars[k] ?? `{${k}}`));
}
